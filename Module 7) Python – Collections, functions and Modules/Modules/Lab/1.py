"""Write a Python program to import the math module and use functions like 
sqrt(), ceil(),floor()."""

import math # using import keyword we can import modules 

num1 = float(input("Enter No : "))#create a var for u input

print("Sqrt = ",math.sqrt(num1))#using sqrt we can find sqrt  
print("Ceil = ",math.ceil(num1))#using ceil we can remove floting point and incress the value accending
print("Floor = ",math.floor(num1))#using floor we can remove floting point and decress the value desending


